    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            conexion = new ConexioSQLite();
            conexion.coneccionbase();
            int resultado = 0;

            String usuario = this.txt_usuario.getText();
            String password = this.txt_contrasena.getText();
            String rolSeleccionado = combo_rol.getSelectedItem().toString();

            if (usuario.equals("") || password.equals("") || combo_rol.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Ingrese Todos los Campos Obligatorios");
            } else {
                char idRol = rolSeleccionado.charAt(0);
                String id = String.valueOf(idRol);

                resultado = conexion.validacionUser(usuario, password, id);

                if (resultado == 0) {
                    JOptionPane.showMessageDialog(null, "Usuario o Password Incorrectos");
                } else {                    
                    Login login = new Login();
                    login.setVisible(false);
                    new Bares(null, true, usuario).setVisible(true);
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

